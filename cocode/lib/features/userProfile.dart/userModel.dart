class userInfo {
  String email;
  String firstName;
  String lastName;
  String major;
  String university;
  String userName;
  userInfo(String email, String firstName, String lastName, String major,
      String university, String userName) {
    this.email = email;
    this.firstName = firstName;
    this.lastName = lastName;
    this.major = major;
    this.university = university;
    this.userName = userName;
  }
}
